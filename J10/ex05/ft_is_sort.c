int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) <= 0)
			j++;
		i++;
	}
	if (length == 0 || length == 1 || j == length - 1)
		return (1);
	return (0);
}
