int	ft_max(int *tab, int length)
{
	int n;
	int nmax;

	n = 1;
	nmax = tab[0];
	while (n < length)
	{
		if (nmax < tab[n])
			nmax = tab[n];
		else
			n++;
	}
	return (nmax);
}
