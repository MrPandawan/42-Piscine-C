int	ft_bouton(int i, int j, int k)
{
	if ((i > j && i < k) || (i > k && i < j))
	{
		return (i);
	}
	else if ((j > i && j < k) || (j > k && j < i))
	{
		return (j);
	}
	else if ((k > i && k < j) || (k > j && k < i))
	{
		return (k);
	}
	else
		return (0);
}
