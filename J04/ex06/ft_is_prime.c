int	ft_is_prime(int nb)
{
	int x;

	if ((nb <= 0) || (nb == 1))
		return (0);
	x = nb - 1;
	while (x > 1)
	{
		if (nb % x == 0)
			return (0);
		else
			x--;
	}
	return (1);
}
