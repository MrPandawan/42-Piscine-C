int	ft_find_next_prime(int nb)
{
	int x;

	if ((nb <= 0) || (nb == 1))
		return (2);
	x = nb - 1;
	while (x > 1)
	{
		if (nb % x == 0)
			nb = nb + 1;
		else
			x--;
	}
	return (nb);
}
