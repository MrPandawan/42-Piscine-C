char	*ft_strrev(char *str)
{
	int		g;
	int		d;
	char	ptr;

	g = 0;
	while (str[g] != '\0')
	{
		g++;
	}
	d = g - 1;
	g = 0;
	while (d > g)
	{
		ptr = str[g];
		str[g] = str[d];
		str[d] = ptr;
		g++;
		d--;
	}
	return (str);
}
