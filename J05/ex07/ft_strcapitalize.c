char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if ((str[0] >= 97) && (str[0] <= 122))
		str[0] = str[0] - 32;
	while (str[i] != '\0')
	{
		if (((str[i] < 65) || (str[i] > 90))
				&& ((str[i] < 48) || (str[i] > 57))
				&& ((str[i] < 97) || (str[i] > 122)))
		{
			if (str[i + 1] >= 97 && str[i + 1] <= 122)
				str[i + 1] = str[i + 1] - 32;
		}
		else if ((str[i + 1] >= 65) && (str[i + 1] <= 90))
			str[i + 1] = str[i + 1] + 32;
		i++;
	}
	return (str);
}
